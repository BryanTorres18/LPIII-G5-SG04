public class Evaluacion {
    int sexo;
    int trabaja;
    int sueldo;

    //Constructor
    public Evaluacion(int sexo, int trabaja, int sueldo)
    {
        this.sexo = sexo;
        this.trabaja = trabaja;
        this.sueldo = sueldo;
    }

    //Getters
    public int getSexo()
    {
        return sexo;
    }
    public int getTrabaja()
    {
        return trabaja;
    }

    public int getSueldo()
    {
        return sueldo;
    }

}
